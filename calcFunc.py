#Functions for calculator
import math #is this cheating?
def add(num1,num2):
    ans = num1 + num2
    return ans
def sub(num1,num2):
    ans = num1 - num2
    return ans
def mult(num1,num2):
    ans = num1 * num2
    return ans
def div(num1,num2):
    ans = num1 / num2
    return ans
def sqr(num1):
    ans = math.sqrt(num1)
    return ans
def csqr(num1):
    ans = math.cbrt(num1)
    return ans
def expo(num1,exp):
    ans = num1 ** exp
    return ans